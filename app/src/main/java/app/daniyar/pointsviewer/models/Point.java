package app.daniyar.pointsviewer.models;

public class Point {
    private float mXValue;
    private float mYValue;

    public float getX() {
        return mXValue;
    }

    public void setX(float mXValue) {
        this.mXValue = mXValue;
    }

    public float getY() {
        return mYValue;
    }

    public void setY(float mYValue) {
        this.mYValue = mYValue;
    }
}
